def add_section(doc, heading, filename, desc, rows, notes=None):
    doc.add_heading(heading, level=1)
    doc.add_paragraph(f'範本檔案：{filename}')
    doc.add_paragraph(f'說明：{desc}')
    doc.add_paragraph('')
    t = doc.add_table(rows=1, cols=5)
    t.style = 'Table Grid'
    # t.alignment = center
    for i, h in enumerate(['Excel儲存格', 'Excel欄位名稱', '網站欄位', '格式說明', '下拉選單值']):
        c = t.rows[0].cells[i]
        c.text = h
        for p in c.paragraphs:
            for r in p.runs:
                r.bold = True
                r.font.size = Pt(9)
    for rd in rows:
        row = t.add_row()
        for i, val in enumerate(rd):
            row.cells[i].text = str(val)
            for p in row.cells[i].paragraphs:
                for r in p.runs:
                    r.font.size = Pt(9)
    if notes:
        doc.add_paragraph('')
        for n in notes:
            doc.add_paragraph(n, style='List Bullet')
    doc.add_page_break()

